def get_dataset_info(dataset_name, remove_h):
    if dataset_name == "qm9":
        if not remove_h:
            return qm9_with_h
        else:
            return qm9_without_h
    elif dataset_name == "drugs":
        if not remove_h:
            return geom_with_h
        else:
            raise Exception("Missing config for %s without hydrogens" % dataset_name)
    elif dataset_name == "qm9_second_half":
        if not remove_h:
            return qm9_second_half
        else:
            raise Exception("Missing config for %s without hydrogens" % dataset_name)
    elif dataset_name == "ligand_pocket":
        return ligand
    elif dataset_name == "aqm":
        return aqm
    elif dataset_name == "spice":
        return spice
    else:
        raise Exception("Wrong dataset %s" % dataset_name)
